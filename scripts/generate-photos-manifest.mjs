import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { imageSize } from "image-size";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const photosDir = join(root, "public", "photos");
const outFile = join(root, "app", "data", "photos.ts");

const files = readdirSync(photosDir)
  .filter((f) => /\.(jpe?g|png|webp|avif)$/i.test(f))
  .sort();

const items = files.map((file) => {
  const buf = readFileSync(join(photosDir, file));
  const { width, height } = imageSize(buf);
  return { src: `/photos/${file}`, w: width, h: height };
});

if (!existsSync(join(root, "app", "data"))) {
  mkdirSync(join(root, "app", "data"), { recursive: true });
}

const ts = `// Auto-generated by scripts/generate-photos-manifest.mjs — do not edit by hand.
export type Photo = { src: string; w: number; h: number };

export const photos: Photo[] = ${JSON.stringify(items, null, 2)};
`;

writeFileSync(outFile, ts);
console.log(`Wrote ${items.length} photos to ${outFile}`);
